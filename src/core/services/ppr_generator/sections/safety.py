"""PPR Generator — Раздел 8: Охрана труда, ПБ, экология."""
from typing import List
from ..schemas import PPRInput, SectionResult, TTKResult


def generate_safety(input: PPRInput, ttks: List[TTKResult]) -> SectionResult:
    profession_rules = {
        "Сварщик": ["Проверить исправность сварочного оборудования", "Надеть СИЗ (маска, краги, спецобувь)", "Проверить заземление", "Оградить место сварки"],
        "Монтажник": ["Проверить страховочную привязь", "Осмотреть монтажный пояс", "Проверить исправность лестниц и подмостей", "Оградить опасную зону"],
        "Машинист": ["Проверить исправность тормозной системы", "Осмотреть грузозахватные приспособления", "Проверить ограничитель грузоподъёмности", "Убедиться в отсутствии людей в опасной зоне"],
        "Бетонщик": ["Надеть СИЗ (каска, перчатки, очки)", "Проверить исправность вибратора", "Осмотреть опалубку"],
        "Стропальщик": ["Осмотреть стропы и захваты", "Проверить бирки на стропах", "Надеть каску и сигнальный жилет"],
        "Землекоп": ["Проверить крепление стенок котлована", "Надеть каску и спецобувь", "Установить ограждение котлована"],
        "Маляр": ["Проверить вентиляцию", "Надеть респиратор и очки", "Убрать источники открытого огня"],
        "Геодезист": ["Надеть каску и сигнальный жилет", "Согласовать работу с крановщиком"],
    }

    # Collect unique professions from TTKs
    seen = set()
    rules_sections = []
    for ttk in ttks:
        for w in ttk.resources.workers:
            for prof, rules in profession_rules.items():
                if prof.lower() in w.name.lower() and prof not in seen:
                    seen.add(prof)
                    rules_sections.append("### " + prof + "\n" + "\n".join("- " + r for r in rules))

    rules_text = "".join(rules_sections) if rules_sections else "- Проверить исправность инструмента и СИЗ\n- Получить наряд-допуск"

    content = """## 8. Охрана труда, промышленная безопасность и экология

### 8.1 Общие требования охраны труда
К производству работ допускаются лица не моложе 18 лет, прошедшие медицинский осмотр,
вводный инструктаж, первичный инструктаж на рабочем месте, стажировку и проверку знаний.

Все работники обеспечиваются средствами индивидуальной защиты (СИЗ) согласно
Типовым отраслевым нормам. На стройплощадке обязательно ношение касок.

### 8.2 Требования перед началом работ
{rules_text}

### 8.3 Требования во время работ
- Соблюдать технологическую последовательность операций согласно ТТК
- Не допускать нахождения посторонних в опасной зоне
- Контролировать устойчивость конструкций при монтаже
- При ветре > 12 м/с прекратить крановые работы

### 8.4 Требования в аварийных ситуациях
- Немедленно прекратить работы при возникновении угрозы
- Сообщить руководителю работ и в службу охраны труда
- Оказать первую помощь пострадавшим
- Сохранить обстановку до прибытия комиссии

### 8.5 Требования по окончании работ
- Отключить оборудование от сети
- Убрать инструмент и приспособления
- Очистить рабочее место от мусора

### 8.6 Пожарная безопасность
- На стройплощадке оборудовать пожарный щит
- Обеспечить наличие огнетушителей в местах сварочных работ
- Назначить ответственного за пожарную безопасность

### 8.7 Охрана окружающей среды
- Сбор строительных отходов в специальные контейнеры
- Недопущение проливов ГСМ на грунт
- Мойка колёс автотранспорта на выезде со стройплощадки
""".format(rules_text=rules_text)

    return SectionResult(
        section_id="safety",
        title="8. Охрана труда, ПБ, экология",
        content=content,
        page_count=max(1, len(content) // 3000),
        metadata={"professions_covered": list(seen)},
    )
