"""
PPR Generator — TTK: Устройство шпунтового ограждения (sheet_pile).

Типовая технологическая карта на погружение шпунта Ларсена
вибропогружателем с устройством распределительного пояса.
"""
from __future__ import annotations

from typing import List

from ..ttk_base import BaseTTKGenerator, TTKRegistry
from ...schemas import (
    PPRInput, TTKResult, TTKScope, TTKTechnology,
    TTKQuality, TTKResources,
)


class TTKSheetPile(BaseTTKGenerator):
    """
    ТТК на устройство шпунтового ограждения.

    Область применения: ограждение котлованов, берегоукрепление,
    подпорные стенки в грунтах I–III категории.
    """

    # ── Metadata ──────────────────────────────────────────────────────────

    @property
    def work_type(self) -> str:
        return "sheet_pile"

    @property
    def title(self) -> str:
        return "Устройство шпунтового ограждения"

    # ── Generation Steps ──────────────────────────────────────────────────

    def generate_scope(self, input: PPRInput) -> TTKScope:
        wt = self._find_work_type(input, "sheet_pile")
        volume_desc = f"{wt.volume}" if wt and wt.volume else "согласно проекту"

        return TTKScope(
            work_type=self.work_type,
            description=(
                "ТТК распространяется на устройство шпунтового ограждения "
                "из шпунта Ларсена при строительстве подземных сооружений, "
                "ограждении котлованов и берегоукрепительных работах."
            ),
            applicable_objects=[
                "ограждение котлованов глубиной до 12 м",
                "берегоукрепление и набережные",
                "подпорные стены подземных паркингов",
                "противофильтрационные завесы",
            ],
            climate_zone="II–IV (СНиП 23-01)",
            work_conditions=(
                f"Объём работ: {volume_desc}. "
                "Работы ведутся в одну смену. Грунты I–III группы "
                "по трудности разработки. Уровень грунтовых вод — "
                "ниже отметки погружения шпунта."
            ),
        )

