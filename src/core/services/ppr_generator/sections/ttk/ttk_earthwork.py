"""
PPR Generator — TTK: Земляные работы.

Технологическая карта на комплекс земляных работ:
геодезическая разбивка, снятие растительного слоя,
разработка грунта экскаватором, доработка вручную,
обратная засыпка с послойным уплотнением.
"""
from __future__ import annotations

from typing import List

from ..ttk_base import BaseTTKGenerator, TTKRegistry
from ...schemas import (
    PPRInput,
    TTKResult,
    TTKScope,
    TTKTechnology,
    TTKQuality,
    TTKResources,
    TTKOperation,
    TTKQualityCheck,
)


class TTKEarthwork(BaseTTKGenerator):
    """
    Генератор ТТК «Земляные работы».

    Охватывает полный цикл земляных работ при устройстве
    котлованов, траншей, выемок и обратных засыпок:
    геодезическая разбивка, снятие растительного слоя
    бульдозером, разработка грунта экскаватором с погрузкой
    в автосамосвалы, доработка основания вручную, обратная
    засыпка пазух с послойным уплотнением виброплитой.
    """

    @property
    def work_type(self) -> str:
        return "earthwork"

    @property
    def title(self) -> str:
        return "Земляные работы"

    # ── Step 1: Scope ──────────────────────────────────────────────────

    def generate_scope(self, input: PPRInput) -> TTKScope:
        wt = self._find_work_type(input, "earthwork")
        volume_desc = f"{wt.volume}" if wt and wt.volume else "согласно проекту"

        return TTKScope(
            work_type=self.work_type,
            description=(
                "ТТК распространяется на комплекс земляных работ при "
                "устройстве котлованов, траншей, выемок и обратных "
                "засыпок в соответствии с проектной документацией, "
                "СП 45.13330.2017 «Земляные сооружения, основания и "
                "фундаменты» и СНиП 3.02.01-87."
            ),
            applicable_objects=[
                "Котлованы под фундаменты зданий и сооружений",
                "Траншеи под наружные инженерные сети",
                "Выемки под дорожные одежды и площадки",
                "Обратные засыпки пазух фундаментов и траншей",
                "Планировочные насыпи и подсыпки",
            ],
            climate_zone="Умеренный климатический район",
            work_conditions=(
                f"Объём работ: {volume_desc}. "
                "Температура окружающего воздуха: от +5°C (для "
                "уплотнения связных грунтов) и не ниже −20°C "
                "(для мёрзлых грунтов по спецпроекту). "
                "Отсутствие грунтовых вод выше подошвы выемки. "
                "Грунты — II–III группы по трудности разработки "
                "(ЕНиР Е2-1). Работы на открытой строительной площадке."
            ),
        )

    # ── Step 2: Technology ─────────────────────────────────────────────

    def generate_technology(self, input: PPRInput, scope: TTKScope) -> TTKTechnology:
        ops: List[TTKOperation] = []

        # 1. Геодезическая разбивка
        ops.append(self._make_op(
            seq=1,
            name="Геодезическая разбивка контура выемки",
            desc=(
                "Вынос в натуру осей и границ котлована/траншеи согласно "
                "разбивочному чертежу с закреплением контура на местности "
                "выносными кольями или створными знаками. Фиксация высотных "
                "отметок нивелиром. Составление акта разбивки осей и "
                "исполнительной геодезической схемы."
            ),
            equipment=["нивелир"],
            workers=["геодезист", "землекоп"],
            duration=2.0,
            checkpoint=True,
        ))

        # 2. Снятие растительного слоя
        ops.append(self._make_op(
            seq=2,
            name="Снятие растительного слоя бульдозером",
            desc=(
                "Снятие плодородного растительного слоя грунта бульдозером "
                "на глубину 150–300 мм (по проекту) с перемещением в "
                "отвал на расстояние до 30 м. Растительный грунт "
                "складируется в бурты для последующего использования "
                "при благоустройстве и рекультивации. Срезка производится "
                "продольными проходами с перекрытием полос 200–300 мм."
            ),
            equipment=["бульдозер"],
            workers=["бульдозерист"],
            duration=2.0,
        ))

        # 3. Разработка грунта экскаватором
        ops.append(self._make_op(
            seq=3,
            name="Разработка грунта экскаватором с погрузкой в автосамосвалы",
            desc=(
                "Разработка грунта экскаватором обратная лопата (V ковша "
                "0,5–1,0 м³) послойными проходками. Разработка ведётся "
                "торцевым или лобовым забоем. Недобор до проектной отметки "
                "составляет 100–200 мм (для последующей доработки вручную/ "
                "мини-техникой). Грунт грузится в автосамосвалы и "
                "вывозится в отвал или на полигон. Откосы формируются "
                "согласно проекту с соблюдением угла естественного откоса."
            ),
            equipment=["экскаватор"],
            workers=["экскаваторщик"],
            duration=6.0,
        ))

        # 4. Доработка основания вручную
        ops.append(self._make_op(
            seq=4,
            name="Доработка основания вручную до проектной отметки",
            desc=(
                "Зачистка дна котлована/траншеи вручную штыковыми и "
                "совковыми лопатами с удалением недобора до проектной "
                "отметки. Контроль высотных отметок нивелиром с шагом "
                "2–3 м в продольном и поперечном направлении. Удаление "
                "рыхлого и разуплотнённого грунта. При обнаружении "
                "переборов — подсыпка с послойным уплотнением до "
                "проектной плотности."
            ),
            equipment=["нивелир"],
            workers=["землекоп", "землекоп"],
            duration=3.0,
            checkpoint=True,
        ))

        # 5. Обратная засыпка — 1-й слой
        ops.append(self._make_op(
            seq=5,
            name="Обратная засыпка пазух 1-м слоем (200–300 мм)",
            desc=(
                "Обратная засыпка пазух фундаментов/траншей местным "
                "непучинистым грунтом (песок средней крупности или ПГС) "
                "с разравниванием слоями толщиной 200–300 мм (в рыхлом "
                "состоянии). Подача грунта бульдозером или экскаватором, "
                "разравнивание вручную. Первый слой — нижняя зона пазух."
            ),
            equipment=["бульдозер"],
            workers=["бульдозерист", "землекоп", "землекоп"],
            duration=2.0,
        ))

        # 6. Уплотнение 1-го слоя
        ops.append(self._make_op(
            seq=6,
            name="Послойное уплотнение 1-го слоя виброплитой",
            desc=(
                "Уплотнение отсыпанного слоя грунта виброплитой "
                "(масса 100–200 кг, глубина уплотнения до 300 мм). "
                "Количество проходов — не менее 4–6 по одному следу "
                "с перекрытием полос 150–200 мм. Уплотнение ведётся "
                "от краёв к середине. Коэффициент уплотнения — "
                "не менее 0,95 (по проекту). Контроль плотности "
                "методом режущего кольца или динамическим "
                "плотномером."
            ),
            equipment=["виброплита"],
            workers=["землекоп"],
            duration=1.5,
            checkpoint=True,
        ))

        # 7. Обратная засыпка — 2-й слой
        ops.append(self._make_op(
            seq=7,
            name="Обратная засыпка пазух 2-м слоем (200–300 мм)",
            desc=(
                "Отсыпка и разравнивание второго слоя обратной "
                "засыпки грунтом однородного состава. Толщина "
                "слоя в рыхлом состоянии — 200–300 мм. "
                "Одновременная засыпка с двух сторон фундамента "
                "для исключения одностороннего давления."
            ),
            equipment=["бульдозер"],
            workers=["бульдозерист", "землекоп", "землекоп"],
            duration=2.0,
        ))

        # 8. Уплотнение 2-го слоя
        ops.append(self._make_op(
            seq=8,
            name="Послойное уплотнение 2-го слоя виброплитой",
            desc=(
                "Уплотнение второго слоя виброплитой с соблюдением "
                "режима: 4–6 проходов по одному следу, перекрытие "
                "полос 150–200 мм. Контроль плотности после "
                "уплотнения."
            ),
            equipment=["виброплита"],
            workers=["землекоп"],
            duration=1.5,
        ))

        # 9. Обратная засыпка — 3-й слой (досыпка до проектной отметки)
        ops.append(self._make_op(
            seq=9,
            name="Обратная засыпка 3-м слоем до проектной отметки",
            desc=(
                "Завершающая отсыпка грунта до проектной отметки "
                "с запасом 50–100 мм на усадку. Разравнивание "
                "поверхности с уклоном для отвода поверхностных вод. "
                "При необходимости — устройство глиняного замка или "
                "противокапиллярной подушки из щебня (по проекту)."
            ),
            equipment=["бульдозер"],
            workers=["бульдозерист", "землекоп"],
            duration=2.0,
        ))

        # 10. Уплотнение 3-го слоя
        ops.append(self._make_op(
            seq=10,
            name="Послойное уплотнение 3-го слоя виброплитой",
            desc=(
                "Окончательное уплотнение верхнего слоя засыпки "
                "виброплитой. Коэффициент уплотнения — 0,95–0,98. "
                "Планировочная отметка поверхности после уплотнения "
                "контролируется нивелиром. Допустимое отклонение "
                "±30 мм от проектной."
            ),
            equipment=["виброплита", "нивелир"],
            workers=["землекоп", "землекоп"],
            duration=1.5,
            checkpoint=True,
        ))

        return TTKTechnology(
            preparatory_work=[
                "Расчистка территории от кустарника, пней, валунов, строительного мусора",
                "Устройство временного водоотвода и ограждение зоны работ",
                "Геодезическая разбивка с выносом осей и закреплением реперов",
                "Устройство временных дорог для проезда техники",
                "Определение мест складирования растительного и минерального грунта",
                "Оформление наряда-допуска для земляных работ",
            ],
            main_operations=ops,
            final_operations=[
                "Инструментальная геодезическая съёмка (исполнительная схема котлована)",
                "Оформление акта освидетельствования скрытых работ на основание",
                "Уборка строительного мусора и планировка прилегающей территории",
                "Консервация откосов (при длительных перерывах) от размыва",
                "Сдача выполненных объёмов техническому надзору заказчика",
            ],
            process_diagram="Схема_технологическая_земляные_работы.dwg",
        )

