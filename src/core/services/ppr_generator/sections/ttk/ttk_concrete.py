class TTKConcrete(BaseTTKGenerator):
    """
    ТТК на бетонные работы.

    Охватывает полный цикл бетонирования: подготовка основания,
    установка опалубки, армирование, укладка бетонной смеси,
    вибрирование, уход за бетоном, распалубка.
    """

    @property
    def work_type(self) -> str:
        return "concrete"

    @property
    def title(self) -> str:
        return "Бетонные работы"

    # ── Scope ──────────────────────────────────────────────────────────

    def generate_scope(self, input: PPRInput) -> TTKScope:
        wt = self._find_work_type(input, "concrete")
        volume_str = f"Объём работ: {wt.volume}" if wt and wt.volume else ""

        return TTKScope(
            work_type=self.work_type,
            description=(
                "Технологическая карта разработана на выполнение комплекса "
                "бетонных работ при устройстве монолитных железобетонных "
                "конструкций (фундаменты, стены, колонны, перекрытия). "
                "Включает подготовку основания, установку опалубки, "
                "армирование, укладку бетонной смеси, вибрирование, "
                "уход за бетоном и распалубку. "
                f"{volume_str}"
            ).strip(),
            applicable_objects=[
                "Монолитные железобетонные фундаменты",
                "Монолитные стены и колонны",
                "Монолитные перекрытия и покрытия",
                "Ростверки и подпорные стены",
            ],
            climate_zone="",
            work_conditions=(
                "Температура окружающего воздуха — не ниже +5 °C "
                "(при отрицательных температурах требуется прогрев бетона "
                "по отдельному регламенту). "
                "Осадки — не допускаются (укладка бетона под защитными "
                "укрытиями). "
                "Скорость ветра — не более 15 м/с при подаче бетононасосом"
            ),
        )

