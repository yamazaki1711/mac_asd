"""
PPR Generator — Раздел: Работа с ПС (подъёмные сооружения).

Описывает организацию работ с применением грузоподъёмных
механизмов, кранов, вышек и прочих ПС на стройплощадке.
"""
from __future__ import annotations

from typing import List

from ..schemas import PPRInput, TTKResult, SectionResult, TTKResource


def generate_ps_works(input: PPRInput, ttks: List[TTKResult]) -> SectionResult:
    """
    Генерирует раздел «Работа с ПС».

    Собирает сводную информацию по грузоподъёмным механизмам,
    задействованным во всех ТТК, проверяет наличие опасных зон
    и стеснённых условий из входных данных ПОС.
    """
    # ── Сбор грузоподъёмной техники из всех ТТК ──
    lifting_machines: List[TTKResource] = []
    seen: set = set()
    for ttk in ttks:
        for m in ttk.resources.machines:
            key = m.name.lower()
            if any(kw in key for kw in (
                "кран", "автокран", "башенный", "манипулятор",
                "подъёмник", "вышка", "люлька", "лебёдка",
                "тельфер", "тали", "погрузчик",
            )):
                if key not in seen:
                    seen.add(key)
                    lifting_machines.append(m)

    # ── Стеснённые условия из ПОС ──
    constraint_lines: List[str] = []
    for c in input.site_constraints:
        constraint_lines.append(
            f"- **{c.constraint_type}**: {c.description}"
        )

    # ── Таблица ПС ──
    ps_rows: List[str] = []
    if lifting_machines:
        ps_rows.append(
            "| № | Наименование ПС | Кол-во | Ед. | Примечание |\n"
            "|---|-----------------|--------|-----|------------|"
        )
        for i, m in enumerate(lifting_machines, 1):
            note = "Грузоподъёмный механизм"
            ps_rows.append(
                f"| {i} | {m.name} | {m.quantity} | {m.unit} | {note} |"
            )
    else:
        ps_rows.append("*Грузоподъёмные механизмы не требуются*")

    ps_table = "\n".join(ps_rows)

    # ── Content (using .format() to avoid f-string backslash issues on Python < 3.12) ──
    constraints_text = "\n".join(constraint_lines) if constraint_lines else "*Стеснённые условия отсутствуют по данным ПОС.*"

    content = """## Работа с подъёмными сооружениями (ПС)

### 1. Общие положения

Настоящий раздел разработан в соответствии с требованиями
ФНиП «Правила безопасности опасных производственных объектов,
на которых используются подъёмные сооружения», утверждённых
приказом Ростехнадзора от 26.11.2020 № 461, и определяет
порядок организации работ с применением грузоподъёмных
механизмов на объекте **{object_name}**.

### 2. Перечень подъёмных сооружений

{ps_table}

### 3. Организация безопасной эксплуатации ПС

- Назначение лица, ответственного за безопасное производство
  работ с применением ПС (приказом по организации).
- Назначение стропальщиков и сигнальщиков из числа обученных
  и аттестованных рабочих.
- Разработка и вывешивание на рабочем месте схем строповки
  грузов и таблицы масс перемещаемых грузов.
- Ограждение опасной зоны работы крана сигнальным ограждением
  и знаками безопасности (ГОСТ 12.4.026).
- Ежесменный осмотр ПС и грузозахватных приспособлений
  перед началом работ с записью в журнале.

### 4. Опасные зоны

При работе кранов и подъёмников опасная зона определяется
радиусом, равным максимальному вылету стрелы плюс ½
наибольшего габарита перемещаемого груза, увеличенному
на минимальное расстояние отлёта груза (по прил. Г к ФНиП № 461).

### 5. Стеснённые условия

{constraints_text}

### 6. Мероприятия по безопасности

- **ППРк (план производства работ кранами)**: разрабатывается
  и утверждается до начала работ.
- **Привязка крана**: определение рабочей и опасной зон,
  привязка к существующим зданиям и коммуникациям.
- **Ограничение зоны обслуживания**: принудительное
  ограничение поворота стрелы (концевые выключатели, упоры).
- **Совместная работа нескольких кранов**: производится
  по отдельному ППРк с назначением координатора.
- **Метеоусловия**: прекращение работ при скорости ветра
  свыше допустимой паспортной величины, гололёде, грозе.

### 7. Грузозахватные приспособления и тара

- Все съёмные грузозахватные приспособления должны иметь
  бирки/клейма с указанием грузоподъёмности и даты испытания.
- Стропы подлежат периодическому осмотру (через каждые 10 дней —
  текстильные; 1 раз в месяц — стальные канатные, цепные).
- Тара для бетона, раствора, сыпучих материалов проверяется
  на целостность перед каждым применением.
""".format(object_name=input.object_name, ps_table=ps_table, constraints_text=constraints_text)

    tables = [
        {
            "title": "Перечень подъёмных сооружений",
            "headers": ["№", "Наименование ПС", "Кол-во", "Ед.", "Примечание"],
            "rows": [
                [str(i), m.name, str(m.quantity), m.unit, "Грузоподъёмный механизм"]
                for i, m in enumerate(lifting_machines, 1)
            ] if lifting_machines else [["—", "Нет", "—", "—", "—"]],
        }
    ]

    return SectionResult(
        section_id="ps_works",
        title="Работа с ПС (подъёмные сооружения)",
        content=content,
        page_count=max(1, len(content.split("\n")) // 40),
        tables=tables,
        metadata={
            "lifting_machines_count": len(lifting_machines),
            "constraints_count": len(input.site_constraints),
        },
    )
