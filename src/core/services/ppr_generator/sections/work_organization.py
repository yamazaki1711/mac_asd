    lines.append("")
    lines.extend([
        "Доставка материалов и конструкций осуществляется автомобильным "
        "транспортом. Основные маршруты согласовываются с ГИБДД и "
        "балансодержателями дорог (при перевозке негабаритных грузов).",
        "",
    ])

    if inp.material_specs:
        lines.append("**Основные материалы и конструкции, подлежащие доставке:**")
        lines.append("")
        for spec in inp.material_specs[:10]:  # не более 10 позиций
            lines.append(
                f"- {spec.name} — {spec.quantity} {spec.unit}"
                f" (ГОСТ/ТУ: {spec.gost})" if spec.gost else
                f"- {spec.name} — {spec.quantity} {spec.unit}"
            )
        if len(inp.material_specs) > 10:
            lines.append(
                f"- ... и ещё {len(inp.material_specs) - 10} позиций "
                f"(см. ведомость материалов)"
            )
        lines.append("")

    # Виды транспорта и механизмов
    lines.append("#### 2.6.2 Транспортные средства и механизмы")
    lines.append("")

    # Собираем машины из ТТК и ресурсных нужд
    machines = set()
    for ttk in ttks:
        for m in ttk.resources.machines:
            machines.add(m.name)
    for m in inp.pos_resource_needs.machines:
        machines.add(m)

    transport_defaults = [
        "Автобетоносмесители (миксеры) на базе КамАЗ / МАЗ",
        "Автобетононасосы (стационарные и на автомобильном шасси)",
        "Автокраны грузоподъёмностью 25–50 т",
        "Гусеничные краны (при слабых грунтах и больших вылетах)",
        "Бортовые автомобили / тягачи с полуприцепами",
        "Самосвалы для доставки инертных материалов",
        "Автотранспорт для доставки арматуры, металлоконструкций, опалубки",
        "Погрузчики фронтальные / вилочные для складских операций",
    ]

    if machines:
        lines.append("**Привлекаемая техника (из ТТК и ресурсной ведомости):**")
        lines.append("")
        for m in sorted(machines)[:15]:
            lines.append(f"- {m}")
        lines.append("")
    else:
        lines.append("**Основные транспортные средства и механизмы:**")
        lines.append("")
        for item in transport_defaults:
            lines.append(f"- {item}")
        lines.append("")

    # Организация движения
    lines.append("#### 2.6.3 Организация движения на площадке")
    lines.append("")
    lines.extend([
        "Движение автотранспорта на строительной площадке организуется "
        "по кольцевой (или тупиковой с разворотными площадками) схеме.",
        "",
        "**Основные требования:**",
        "",
        "- Скорость движения по строительной площадке — не более 10 км/ч",
        "- Ширина временных дорог: 3,5 м при одностороннем движении, "
        "6,0 м при двустороннем",
        "- Радиусы закруглений — не менее 12 м",
        "- Установка дорожных знаков согласно ПДД и ГОСТ Р 52289-2004",
        "- Мойка колёс автотранспорта на выезде со стройплощадки "
        "(устройство пункта мойки колёс с оборотным водоснабжением)",
        "- Ограничение въезда постороннего транспорта (шлагбаум, охрана)",
        "",
    ])

    # График поставок
    lines.append("#### 2.6.4 График поставок")
    lines.append("")
    lines.extend([
        "Поставки материалов осуществляются в соответствии с "
        "календарным графиком производства работ. Доставка материалов "
        "на объект производится «с колёс» (преимущественно) для "
        "минимизации складских запасов, за исключением материалов, "
        "требующих технологической выдержки (цемент, бетонные смеси "
        "с добавками).",
        "",
        "Негабаритные и длинномерные грузы доставляются в ночное время "
        "по согласованным маршрутам в сопровождении автомобиля "
        "прикрытия ГИБДД.",
        "",
    ])

    return "\n".join(lines)


