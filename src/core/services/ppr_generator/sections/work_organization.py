"""
PPR Generator — Section: Work Organization (Организация производства работ).

Раздел 2 Пояснительной записки ППР: подготовительный период,
решения по стройплощадке, методы производства, геодезия,
зимнее бетонирование, транспорт.
"""

from __future__ import annotations

from typing import List

from ..schemas import PPRInput, SectionResult, TTKResult

__all__ = ["generate_org"]


def generate_org(input: PPRInput, ttks: List[TTKResult]) -> SectionResult:
    """
    Генерирует раздел «2. Организация производства работ» ПЗ ППР.

    Содержит:
      - Подготовительный период
      - Решения по организации стройплощадки
      - Методы производства основных видов работ (из ТТК)
      - Геодезическое обеспечение
      - Производство работ в зимних условиях (зимнее бетонирование)
      - Решения по транспорту и логистике
    """
    md = _build_content(input, ttks)
    return SectionResult(
        section_id="work_organization",
        title="2. Организация производства работ",
        content=md,
        page_count=_estimate_pages(md),
    )


