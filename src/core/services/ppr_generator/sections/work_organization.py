# ── 2.3 Методы производства работ ─────────────────────────────────────

def _work_methods(inp: PPRInput, ttks: List[TTKResult]) -> str:
    lines = [
        "### 2.3 Методы производства основных видов работ",
        "",
        "Методы производства работ определены на основе "
        "проектной и рабочей документации, проекта организации "
        "строительства и разработанных технологических карт (ТТК).",
        "",
    ]

    if not ttks:
        lines.append(
            "Перечень ТТК и методов производства уточняется "
            "при разработке рабочей документации."
        )
        lines.append("")
        return "\n".join(lines)

    lines.append("#### 2.3.1 Применяемые технологические карты")
    lines.append("")

    # Таблица сводки ТТК
    lines.append("| № п/п | Технологическая карта | Основные операции |")
    lines.append("|---|---|---|")
    for i, ttk in enumerate(ttks, 1):
        ops = [
            op.name for op in ttk.technology.main_operations[:3]
        ]
        ops_summary = " → ".join(ops) if ops else "—"
        lines.append(f"| {i} | {ttk.work_type} | {ops_summary} |")

    lines.append("")

    # Детализация по каждой ТТК
    lines.append("#### 2.3.2 Детализация технологических процессов")
    lines.append("")

    for i, ttk in enumerate(ttks, 1):
        lines.append(f"**{i}. {ttk.work_type}**")
        lines.append("")

        # Область применения
        lines.append(f"*Область применения:* {ttk.scope.description}")
        lines.append("")

        # Подготовительные работы ТТК
        prep = ttk.technology.preparatory_work
        if prep:
            lines.append("*Подготовительные работы:*")
            lines.append("")
            for p in prep:
                lines.append(f"- {p}")
            lines.append("")

        # Основные операции
        ops = ttk.technology.main_operations
        if ops:
            lines.append("*Основные технологические операции:*")
            lines.append("")
            lines.append(
                "| № | Операция | Оборудование | Бригада | "
                "Продолж. (ч) | Контроль |"
            )
            lines.append("|---|---|---|---|---|---|")
            for op in ops:
                equip = ", ".join(op.equipment[:2]) if op.equipment else "—"
                workers = ", ".join(op.workers[:3]) if op.workers else "—"
                chk = "✓" if op.quality_checkpoint else "—"
                lines.append(
                    f"| {op.seq_number} | {op.name} | "
                    f"{equip} | {workers} | "
                    f"{op.duration_hours} | {chk} |"
                )
            lines.append("")

        # Ресурсы
        res = ttk.resources
        if res.workers or res.machines:
            lines.append("*Основные ресурсы:*")
            lines.append("")
            if res.workers:
                workers_str = ", ".join(
                    f"{w.name} — {w.quantity} {w.unit}"
                    for w in res.workers[:5]
                )
                lines.append(f"- Рабочие: {workers_str}")
            if res.machines:
                machines_str = ", ".join(
                    f"{m.name} — {m.quantity} {m.unit}"
                    for m in res.machines[:5]
                )
                lines.append(f"- Машины и механизмы: {machines_str}")
            lines.append("")

        lines.append("---")
        lines.append("")

    lines.append("")
    return "\n".join(lines)


