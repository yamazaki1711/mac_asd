# ═══════════════════════════════════════════════════════════════════════

def _build_content(inp: PPRInput, ttks: List[TTKResult]) -> str:
    parts: List[str] = []

    parts.append("## 2. Организация производства работ\n")
    parts.append(_preparatory_period(inp))
    parts.append(_construction_site(inp))
    parts.append(_work_methods(inp, ttks))
    parts.append(_geodetic_support(inp))
    parts.append(_winter_concreting(inp))
    parts.append(_transport_solutions(inp, ttks))

    return "\n\n".join(parts)


