    async def _execute(self, params: Dict[str, Any]) -> SkillResult:
        """
        Выполнить проверку рисков.

        params:
            action: "list_traps" | "check_outdated" | "checklist" | "search_traps"
            work_type: вид работ для фильтрации
            query: поисковый запрос (для search_traps)
            text: текст для проверки устаревших ссылок (для check_outdated)
        """
        action = params.get("action", "list_traps")
        work_type = params.get("work_type")

        if action == "list_traps":
            return self._list_traps(work_type)
        elif action == "check_outdated":
            return self._check_outdated(params.get("text", ""))
        elif action == "checklist":
            return self._get_checklist()
        elif action == "search_traps":
            return self._search_traps(params.get("query", ""), work_type)
        else:
            return SkillResult(
                status=SkillStatus.ERROR,
                skill_id=self.skill_id,
                errors=[f"Unknown action: {action}"],
            )

    def _list_traps(self, work_type: Optional[str] = None) -> SkillResult:
        """Получить список ловушек, отфильтрованный по виду работ."""
        traps = self.BLC_SEED_DATA

        if work_type:
            traps = [
                t for t in traps
                if work_type in t.get("work_types", [])
                or "общестроительные" in t.get("work_types", [])
            ]

        return SkillResult(
            status=SkillStatus.SUCCESS,
            skill_id=self.skill_id,
            data={
                "total_traps": len(traps),
                "work_type_filter": work_type,
                "traps": traps,
            },
        )

    def _check_outdated(self, text: str) -> SkillResult:
        """Проверить текст на устаревшие нормативные ссылки."""
        found = []

        for ref, info in self.OUTDATED_REFS.items():
            if ref in text:
                found.append({
                    "outdated_ref": ref,
                    "replaced_by": info["replaced_by"],
                    "reason": info["reason"],
                    "severity": info["severity"],
                })

        return SkillResult(
            status=SkillStatus.SUCCESS,
            skill_id=self.skill_id,
            data={
                "text_checked": len(text),
                "outdated_refs_found": len(found),
                "outdated_refs": found,
            },
        )

    def _get_checklist(self) -> SkillResult:
        """Получить чек-лист для экспертизы договора."""
        return SkillResult(
            status=SkillStatus.SUCCESS,
            skill_id=self.skill_id,
            data={
                "checklist_items": len(self.CONTRACT_CHECKLIST),
                "checklist": self.CONTRACT_CHECKLIST,
                "price_revision_threshold_pct": self.PRICE_REVISION_THRESHOLD_PCT,
            },
        )

    def _search_traps(self, query: str, work_type: Optional[str] = None) -> SkillResult:
        """Простой поиск ловушек по ключевым словам."""
        query_lower = query.lower()
        results = []

        for trap in self.BLC_SEED_DATA:
            # Check work type filter
            if work_type and work_type not in trap.get("work_types", []):
                continue

            # Simple keyword matching
            searchable = f"{trap['title']} {trap['description']} {trap['legal_basis']}".lower()
            if query_lower in searchable:
                results.append(trap)

        return SkillResult(
            status=SkillStatus.SUCCESS,
            skill_id=self.skill_id,
            data={
                "query": query,
                "work_type_filter": work_type,
                "results_found": len(results),
                "results": results,
            },
        )
