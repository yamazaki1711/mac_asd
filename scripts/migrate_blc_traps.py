def main():
    parser = argparse.ArgumentParser(description="Migrate 61 curated БЛС traps → PostgreSQL")
    parser.add_argument("--dry-run", action="store_true", help="Preview without writing")
    args = parser.parse_args()

    from src.core.knowledge.knowledge_base import knowledge_base

    indexed = 0
    duplicates = 0
    errors = 0

    for i, trap in enumerate(CURATED_TRAPS, 1):
        logger.info(
            "[%s] [%d/61] %s: %s",
            "DRY-RUN" if args.dry_run else "WRITE",
            i,
            trap["category"],
            trap["title"][:100],
        )

        if args.dry_run:
            indexed += 1
            continue

        try:
            trap_id = knowledge_base.index_trap(
                domain="legal",
                title=trap["title"],
                description=trap["description"],
                source="curated",
                channel="",
                category=trap["category"],
                weight=trap["weight"],
                mitigation=trap["mitigation"],
            )
            if trap_id:
                indexed += 1
            else:
                duplicates += 1
                logger.warning("  Duplicate: %s", trap["title"][:80])
        except Exception as e:
            logger.error("  Failed: %s", e)
            errors += 1

    print(f"\n=== Migration Summary ===")
    print(f"Indexed:    {indexed}")
    print(f"Duplicates: {duplicates}")
    print(f"Errors:     {errors}")
    print(f"Total:      {indexed + duplicates + errors}/61")


if __name__ == "__main__":
    main()
