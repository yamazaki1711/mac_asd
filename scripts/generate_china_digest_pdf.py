"""
Generate PDF digest: Chinese Document Automation Experience for MAC_ASD v12.0.
Output: docs/China_AI_Construction_Digest.pdf (~8 A4 pages)
"""

import os
from fpdf import FPDF

# ---------------------------------------------------------------------------
# Paths
# ---------------------------------------------------------------------------
# Try multiple locations for a Unicode font with Cyrillic support
_CANDIDATES = [
    "C:/Windows/Fonts/ARIALUNI.ttf",                         # Windows — Arial Unicode MS
    "/c/Windows/Fonts/ARIALUNI.ttf",                          # WSL on Windows
    "/usr/share/fonts/truetype/dejavu/DejaVuSans.ttf",        # Linux
    "/usr/share/fonts/truetype/liberation/LiberationSans-Regular.ttf",  # Linux
    "/System/Library/Fonts/Supplemental/Arial Unicode.ttf",   # macOS
]

FONT_PATH = None
for candidate in _CANDIDATES:
    if os.path.exists(candidate):
        FONT_PATH = candidate
        break

if FONT_PATH is None:
    raise FileNotFoundError(
        "No Unicode font found. Tried: " + ", ".join(_CANDIDATES[:3]) + " ..."
    )

OUTPUT_DIR = os.path.join(os.path.dirname(os.path.dirname(os.path.abspath(__file__))), "docs")
OUTPUT_FILE = os.path.join(OUTPUT_DIR, "China_AI_Construction_Digest.pdf")


class DigestPDF(FPDF):
    """Custom PDF with headers, body, tables, and bullet lists."""

    def __init__(self):
        super().__init__(orientation="P", unit="mm", format="A4")
        self.add_font("Uni", "", FONT_PATH)
        self.add_font("Uni", "B", FONT_PATH)
        self.set_auto_page_break(True, margin=20)
        # Margins
        self.set_left_margin(25)
        self.set_right_margin(20)
        self.set_top_margin(20)

    # ------------------------------------------------------------------
    # Layout helpers
    # ------------------------------------------------------------------
    def title_page(self, title: str, subtitle: str, date: str):
        self.add_page()
        self.ln(40)
        self.set_font("Uni", "B", 24)
        self.multi_cell(0, 12, title, align="C")
        self.ln(8)
        self.set_font("Uni", "", 14)
        self.multi_cell(0, 8, subtitle, align="C")
        self.ln(20)
        self.set_font("Uni", "", 11)
        self.cell(0, 7, date, align="C")
        self.ln(14)
        # Horizontal rule
        x0 = self.get_x()
        y0 = self.get_y()
        self.line(50, y0, 160, y0)
        self.ln(8)
        self.set_font("Uni", "", 9)
        self.multi_cell(0, 6,
            "Подготовлено в рамках проекта MAC_ASD v12.0\n"
            "Multi-Agent Construction AI System — Автоматизация субподрядного документооборота",
            align="C",
        )

    def h1(self, text: str):
        self.ln(4)
        self.set_font("Uni", "B", 15)
        self.multi_cell(0, 8, text)
        self.ln(1)
        # Thin rule
        y = self.get_y()
        self.line(self.l_margin, y, self.w - self.r_margin, y)
        self.ln(3)

    def h2(self, text: str):
        self.ln(3)
        self.set_font("Uni", "B", 12)
        self.multi_cell(0, 7, text)
        self.ln(1)

    def h3(self, text: str):
        self.ln(2)
        self.set_font("Uni", "B", 10.5)
        self.multi_cell(0, 6, text)
        self.ln(1)

    def body(self, text: str):
        self.set_font("Uni", "", 9.5)
        self.set_x(self.l_margin)
        self.multi_cell(0, 5.5, text, align="J")

    def bullet(self, text: str, indent: int = 5):
        self.set_font("Uni", "", 9.5)
        x = self.l_margin + indent
        self.set_x(x)
        bullet_char = "•"
        self.cell(5, 5.5, bullet_char)
        self.multi_cell(self.w - self.r_margin - x - 5, 5.5, text, align="J")
        self.set_x(self.l_margin)  # reset after custom-width multi_cell

    def numbered(self, num: str, text: str, indent: int = 5):
        self.set_font("Uni", "", 9.5)
        x = self.l_margin + indent
        self.set_x(x)
        self.cell(7, 5.5, num)
        self.multi_cell(self.w - self.r_margin - x - 7, 5.5, text, align="J")
        self.set_x(self.l_margin)  # reset after custom-width multi_cell

    def bold_body(self, text: str):
        self.set_font("Uni", "B", 9.5)
        self.set_x(self.l_margin)
        self.multi_cell(0, 5.5, text, align="J")

    def code_block(self, text: str):
        self.ln(1)
        self.set_fill_color(245, 245, 245)
        self.set_font("Uni", "", 8)
        for line in text.strip().split("\n"):
            self.set_x(self.l_margin + 5)
            self.cell(self.w - self.l_margin - self.r_margin - 10, 4.5, line, fill=True)
            self.ln()
        self.ln(2)

    def simple_table(self, headers: list, rows: list, col_widths: list = None):
        """Draw a simple table with header row."""
        if col_widths is None:
            usable = self.w - self.l_margin - self.r_margin
            col_widths = [usable / len(headers)] * len(headers)

        # Header
        self.set_fill_color(52, 73, 94)
        self.set_text_color(255, 255, 255)
        self.set_font("Uni", "B", 8.5)
        for i, h in enumerate(headers):
            self.cell(col_widths[i], 7, " " + h, border=0, fill=True)
        self.ln()

        # Rows
        self.set_text_color(0, 0, 0)
        for ri, row in enumerate(rows):
            if ri % 2 == 0:
                self.set_fill_color(248, 248, 248)
            else:
                self.set_fill_color(255, 255, 255)
            self.set_font("Uni", "", 8)
            for i, cell in enumerate(row):
                self.cell(col_widths[i], 6, " " + str(cell), border=0, fill=True)
            self.ln()
        self.set_text_color(0, 0, 0)
        self.ln(2)

    # ------------------------------------------------------------------
    # Footer
    # ------------------------------------------------------------------
    def footer(self):
        self.set_y(-18)
        self.set_font("Uni", "", 7)
        self.set_text_color(128, 128, 128)
        self.cell(0, 10, f"MAC_ASD v12.0 — Chinese AI in Construction Digest — стр. {self.page_no()}/{{nb}}", align="C")


